package javasessions;

public class SwitchExample {

	public static void main(String[] args) {
		 int temp=28;
		 
		 switch (temp) {
		 case (50):
			 System.out.println("It is HOT ");
			 break;
		 case (0):
			 System.out.println("freezing");
			 break;	 
			 
		 case(28):
			 System.out.println("pleasant");
			 break;
		 }
	}

}
